import joblib
import pandas as pd

print("=" * 40)
print("УЗНАЙ СВОЙ ТИП ЛИЧНОСТИ")
print("=" * 40)

print("\nЗагружаю модель...")
model = joblib.load('kmeans_model.pkl')
print("Модель загружена!")

print("\nОтвечай на вопросы от 0 до 10")
print("0 - совсем не про меня, 10 - это точно про меня")
print("-" * 40)

print("\n1. Как сильно ты любишь общаться с людьми?")
print("   (0 - ненавижу, 10 - обожаю)")
social = float(input("   Твой ответ: "))

print("\n2. Как часто ты хочешь побыть один/одна?")
print("   (0 - никогда, 10 - постоянно)")
alone = float(input("   Твой ответ: "))

print("\n3. Насколько ты разговорчив/разговорчива?")
print("   (0 - молчун, 10 - говорю без остановки)")
talk = float(input("   Твой ответ: "))

user_data = [[social, alone, talk]]
cluster = model.predict(user_data)[0]

print("\n" + "=" * 40)
print("ТВОЙ РЕЗУЛЬТАТ")
print("=" * 40)

if cluster == 0:
    print("\n🔴 Ты в КРАСНОМ кластере (Кластер 0)")
    print("\n🏷️  Это ИНТРОВЕРТ!")
    print("\n📖 Что это значит:")
    print("   • Ты не очень любишь шумные компании")
    print("   • Тебе нужно время побыть одному/одной")
    print("   • Ты больше слушаешь, чем говоришь")
    print("   • У тебя мало, но очень близких друзей")

elif cluster == 1:
    print("\n🟢 Ты в ЗЕЛЁНОМ кластере (Кластер 1)")
    print("\n🏷️  Это АМБИВЕРТ!")
    print("\n📖 Что это значит:")
    print("   • Ты - золотая середина!")
    print("   • Можешь быть душой компании, но и дома посидеть любишь")
    print("   • Умеешь и говорить, и слушать")
    print("   • Тебе комфортно в любой ситуации")

else:
    print("\n🔵 Ты в СИНЕМ кластере (Кластер 2)")
    print("\n🏷️  Это ЭКСТРАВЕРТ!")
    print("\n📖 Что это значит:")
    print("   • Ты обожаешь быть в компании!")
    print("   • Новые знакомства - твоё всё")
    print("   • Ты очень разговорчив/разговорчива")
    print("   • Одиночество тебя угнетает")

print("\n" + "=" * 40)
print("Спасибо! Запусти снова, чтобы проверить друга")
print("=" * 40)